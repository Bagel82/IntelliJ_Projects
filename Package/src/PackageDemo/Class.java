package PackageDemo;
public class Class {
    public int a;
    public int b;
    private int c;
    private int d;
    public Class() {
        a = 2;
        b = 45;
        c = 30;
        d = 12;
    }
    public void Class1() {
        System.out.println("Success");
    }
    private void Class2() {
        System.out.println("Success");
    }
    void Class3() {
        System.out.println("Success");
    }
    protected void Class4() {
        System.out.println("Success");
    }
}